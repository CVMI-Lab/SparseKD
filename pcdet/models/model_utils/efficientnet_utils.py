def get_act_layer(name):
    if name == 'ReLU':
        return nn.ReLU
    elif name == 'Swish':
        return MemoryEfficientSwish
    elif name == 'LeakyReLU':
        return nn.LeakyReLU
    elif name == 'ELU':
        return nn.ELU
    else:
        raise NotImplementedError
